    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dateFormat.format(datePicker.getDate());
        String description = (String) descriptionCombo.getSelectedItem();
        String amount = amountText.getText();

        AcountModel acountModel = new AcountModel();
        if (incomeCheck.isSelected()) {
            acountModel.setDate(date);
            acountModel.setDescription(description);
            acountModel.setIncome(amount);
        } else {
            acountModel.setDate(date);
            acountModel.setDescription(description);
            acountModel.setExpenditure(amount);
        }
        try {
            boolean addAccount = OrderControllerClient.addAccount(acountModel);
            if (addAccount) {
                JOptionPane.showMessageDialog(this, "Account details Added successfully !");
                dispose();
            }
        } catch (NotBoundException | MalformedURLException | RemoteException | ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddAcount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_doneButtonActionPerformed

