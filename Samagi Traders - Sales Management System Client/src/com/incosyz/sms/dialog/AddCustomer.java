    private void nicNoTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nicNoTextActionPerformed

        try {
            CustomerModel customer = CustomerControllerImplClient.getCustomer(nicNoText.getText());
            if (customer == null) {
                addressLineOneText.requestFocus();
            } else {
                customerNameText.setText(customer.getCustomerName());
                addressLineOneText.setText(customer.getAddressLineOne());
                addressLineTwoText.setText(customer.getAddressLineTwo());
                addressLineThreeText.setText(customer.getAddressLineThree());

                ArrayList<MobileModel> mobile = CustomerControllerImplClient.getMobile(customer.getCustomerId());
                if (mobile != null) {
                    JTextField[] mobileText = {mobileNoOneText, mobileNoTwoText, mobileNoThreeText, mobileNoFourText};
                    mobileNoOneText.setText("");
                    mobileNoTwoText.setText("");
                    mobileNoThreeText.setText("");
                    mobileNoFourText.setText("");
                    for (int i = 0; i < mobile.size(); i++) {
                        mobileText[i].setText(mobile.get(i).getMobileNo());
                    }
                }
                ArrayList<HomePhoneModel> homePhone = CustomerControllerImplClient.getHomePhone(customer.getCustomerId());
                if (homePhone != null) {
                    JTextField[] homePhoneText = {phoneNoOneText, phoneNoTwoText, phoneNoThreeText};
                    phoneNoOneText.setText("");
                    phoneNoTwoText.setText("");
                    phoneNoThreeText.setText("");
                    for (int i = 0; i < homePhone.size(); i++) {
                        homePhoneText[i].setText(homePhone.get(i).getHomePhoneNo());
                    }
                }

            }
        } catch (NotBoundException | MalformedURLException | ClassNotFoundException | SQLException | RemoteException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nicNoTextActionPerformed

