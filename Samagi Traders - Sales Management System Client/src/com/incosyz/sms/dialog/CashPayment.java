    private DefaultTableModel defaultTableModel;
    private double amountTot = 0;

    public void setCash(ArrayList<CashModel> cashModels) {
        for (CashModel cashModel : cashModels) {
            addRow(cashModel);
        }
        totalAmount.setText("Rs " + CurrancyFormat.getCurrancyFormat(amountTot));
    }

    public void addRow(CashModel cashModel) {
        Object[] row = {CurrancyFormat.getCurrancyFormat(cashModel.getCashAmount()), cashModel.getCashDate()};
        defaultTableModel.addRow(row);

        amountTot += Double.parseDouble(cashModel.getCashAmount());
    }
