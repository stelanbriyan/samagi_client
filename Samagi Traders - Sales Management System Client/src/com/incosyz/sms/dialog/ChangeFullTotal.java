    public void setDetail(DealerPrices dealerPrices) {
        this.dealerPrices = dealerPrices;

        bikeAmountText.setText(String.format("%1.2f", Double.parseDouble(dealerPrices.getBikeAmount())));
//        bikeAmount = Double.parseDouble(dealerPrices.getBikeAmount());
        commisionText.setText(String.format("%1.2f", Double.parseDouble(dealerPrices.getCommision())));
//        commision = Double.parseDouble(dealerPrices.getCommision());
        remittanceText.setText(String.format("%1.2f", Double.parseDouble(dealerPrices.getNetRemittance())));

        String otherPayment = dealerPrices.getOtherPayment() ;

        if (otherPayment != null) {
            otherPaymentText.setText(String.format("%1.2f", Double.parseDouble(dealerPrices.getOtherPayment())));
        }else{
            otherPaymentText.setText("0.00");
        }
        
        String note = dealerPrices.getValueNote();
        if (note != null) {
            noteText.setText(note);
        }else{
            noteText.setText("");
        }
        
        
        bikeAmountLabel.setText(dealerPrices.getBikeAmount());
        commisionLabel.setText(dealerPrices.getCommision());
        remittanceLabel.setText(dealerPrices.getNetRemittance());
    }

    public double getCommision() {
        double bikeAmount = 0;
        if (!bikeAmountText.getText().trim().equals("")) {
            bikeAmount = Double.parseDouble(bikeAmountText.getText());
        }
        double otherPayment = 0;
        if (!otherPaymentText.getText().trim().equals("")) {
            otherPayment = Double.parseDouble(otherPaymentText.getText());
        }

        double netRemittance = 0;
        if (!remittanceText.getText().trim().equals("")) {
            netRemittance = Double.parseDouble(remittanceText.getText());
        }

        return bikeAmount - otherPayment - netRemittance;
    }

    public double getOtherpayment() {
        double bikeAmount = 0;
        if (!bikeAmountText.getText().trim().equals("")) {
            bikeAmount = Double.parseDouble(bikeAmountText.getText());
        }
        double commision = 0;
        if (!commisionText.getText().trim().equals("")) {
            commision = Double.parseDouble(commisionText.getText());
        }

        double netRemittance = 0;
        if (!remittanceText.getText().trim().equals("")) {
            netRemittance = Double.parseDouble(remittanceText.getText());
        }

        return bikeAmount - commision - netRemittance;
    }
