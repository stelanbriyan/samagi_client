    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed

        String bikeAmount = bikeAmountText.getText();
        String commision = commisionText.getText();
        String otherPayment = otherPaymentText.getText();
        String remittance = remittanceText.getText();

        if (otherPayment.equals("")) {
            otherPaymentText.setText("0.00");
        }
        if (commisionText.equals("")) {
            commisionText.setText("0.00");
        }

        Validation v = new Validation();
        boolean inputCurrancy = v.checkNumberForCurrency(bikeAmount);
        boolean inputCurrancy1 = v.checkNumberForCurrency(commision);
        boolean inputCurrancy2 = v.checkNumberForCurrency(otherPayment);

        if (inputCurrancy && inputCurrancy1 && inputCurrancy2) {

            boolean countinue = true;
            String message = "";
            if (Double.parseDouble(commisionText.getText()) < 0) {
                countinue = false;
                message += "Commision ,";
            }
            if (Double.parseDouble(bikeAmountText.getText()) < 0) {
                countinue = false;
                message += "Inclusive price ,";
            }
            if (Double.parseDouble(otherPaymentText.getText()) < 0) {
                countinue = false;
                message += "Other payments ,";
            }
            if (message.length() > 3) {
                message = message.substring(0, message.length() - 1) + " wrong format. check inputs again. can't be use minus values !";
            }
            if (countinue) {
                dealerPrices.setBikeAmount(bikeAmount);
                dealerPrices.setCommision(commision);
                dealerPrices.setOtherPayment(otherPayment);
                dealerPrices.setNetRemittance(remittance);
                dealerPrices.setValueNote(noteText.getText());
                manageSaleDetail.setDealerPrices(dealerPrices);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, message);
            }

        } else {

            JOptionPane.showMessageDialog(this, "Input data is not valid. check again !");
        }
    }//GEN-LAST:event_doneButtonActionPerformed

