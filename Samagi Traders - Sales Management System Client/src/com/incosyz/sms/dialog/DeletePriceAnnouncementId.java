    public void setOrders(ArrayList<OrderModel> orders, String modelName) {
        defaultTableModel = (DefaultTableModel) orderTable.getModel();
        jLabel5.setText(String.valueOf(orders.size()));

        for (OrderModel orderModel : orders) {
            Object[] row = {orderModel.getRoundNo(), modelName, orderModel.getCustomerId(), orderModel.getOrderAmount()};
            defaultTableModel.addRow(row);
        }
    }

    public void setHTC(ArrayList<SpecialDealerModel> htc, String modelName) {
        htcModel = (DefaultTableModel) htcTable.getModel();
        jLabel6.setText(String.valueOf(htc.size()));

        for (SpecialDealerModel model : htc) {
            Object[] row = {modelName, model.getSpecialPrice(), model.getDealerMargine()};
            htcModel.addRow(row);
        }
    }

    public void setAnnouncementModel(DefaultTableModel defaultTableModel, int row, String modelId) {
        this.priceAnnouncementTableModel = defaultTableModel;
        this.row = row;
        this.modelId = modelId;
    }
