    public void setDetail(OrderModel orderModel) {
        roundNoText.setText(orderModel.getRoundNo());
        customerText.setText(orderModel.getCustomerId());
        orderDateText.setText(orderModel.getOrderDate());
        amountText.setText("Rs " + CurrancyFormat.getCurrancyFormat(orderModel.getOrderAmount()));
    }

    void setModelToDelete(DefaultTableModel deliveryOrderDetailModel, int selectedRow) {
        this.deliveryOrderDetailModel = deliveryOrderDetailModel;
        this.selectedRow = selectedRow;
    }
