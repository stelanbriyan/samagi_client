    /**
     * First Check already exists. Second get data from user interface send data
     * to server
     *
     * @param evt
     */
    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        if (PriceAnnouncementControllerClient.isPriceAnnouncementExists()) {
            // Get Data From User Interface. & Create Model and send data to Server.
            try {
                try {
                    DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    String priceAnnoucementId = Generator.generateId("PriceAnnouncement", "priceAnnouncementId", "P", 3);
                    String priceAnnoucement = dateFormat.format(priceAnnouncementDate.getDate());
                    String effectDate = dateFormat.format(effectFromDate.getDate());

                    PriceAnnouncementModel priceAnnouncementModel = new PriceAnnouncementModel()
                            .setPriceAnnouncementDate(priceAnnoucement)
                            .setEffectDate(effectDate)
                            .setPriceAnnouncementId(priceAnnoucementId);

                    boolean addPriceAnnouncement = PriceAnnouncementControllerClient.addPriceAnnouncement(priceAnnouncementModel);
                    if (addPriceAnnouncement) {
                        JOptionPane.showMessageDialog(this, "Price Announcement added successfully !");
                        managePriceAnnouncement.addPriceAnnoucement(priceAnnouncementModel);
                        dispose();
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(NewPriceAnnouncement.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (NotBoundException | MalformedURLException | RemoteException ex) {
                Logger.getLogger(NewPriceAnnouncement.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            warningMessage.setVisible(true);
        }
    }//GEN-LAST:event_doneButtonActionPerformed

