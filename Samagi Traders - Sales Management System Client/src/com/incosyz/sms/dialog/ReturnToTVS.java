    private void newPriceAnnouncement9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newPriceAnnouncement9ActionPerformed
        String roundNo = roundNoText.getText();
        String date = dateText.getText();
        String note = noteText.getText();
        try {
            String returnId = Generator.generateId("ReturnStock", "returnId", "R", 3);

            ReturnModel model = new ReturnModel()
                    .setReturnId(returnId)
                    .setRoundNo(roundNo)
                    .setDate(date)
                    .setNote(note);

            boolean returned = StockControllerClient.addToReturn(model);
            if (returned) {
                JOptionPane.showMessageDialog(this, "round no successfully added to return stock !");
            }
        } catch (NotBoundException | MalformedURLException | RemoteException | ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ReturnToTVS.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_newPriceAnnouncement9ActionPerformed

