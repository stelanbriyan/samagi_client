    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed

        CashModel cashModel = null;
        if (cashPayment) {
            cashModel = new CashModel();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            cashModel.setCashAmount(String.valueOf(cashAmount));
            cashModel.setCashDate(dateFormat.format(new Date()));
        }

        ArrayList<CheckModel> checkModels = null;
        if (chequePayment) {
            checkModels = new ArrayList<>();
            for (int i = 0; i < checkTableModel.getRowCount(); i++) {
                String chequeNo = (String) checkTableModel.getValueAt(i, 0);
                String bankName = (String) checkTableModel.getValueAt(i, 1);
                String date = (String) checkTableModel.getValueAt(i, 2);
                String amount = (String) checkTableModel.getValueAt(i, 3);

                CheckModel checkModel = new CheckModel();
                checkModel.setCheckNo(chequeNo);
                checkModel.setBankName(bankName);
                checkModel.setChequeDate(date);
                checkModel.setAmount(amount);
                checkModel.setStateName("");

                checkModels.add(checkModel);
            }
        }

        LeasingModel leasingModel = null;
        if (leasingPayment) {
            String gs = gsCheck.isSelected() ? "1" : "0";
            String mta = mtaCheck.isSelected() ? "1" : "0";
            String mta2 = mta2Check.isSelected() ? "1" : "0";
            String gap = gapCheck.isSelected() ? "1" : "0";
            String doNo = doCheck.isSelected() ? "1" : "0";

            String mta3 = mta3Check.isSelected() ? "1" : "0";
            String idCopy = idCopyCheck.isSelected() ? "1" : "0";
            String doNo2 = do2Check.isSelected() ? "1" : "0";

            leasingModel = new LeasingModel();
            leasingModel.setLeasingAmount(String.valueOf(leasingAmount));
            leasingModel.setGaranter(garanerDetail.getText());
            String leasingName = "0";
            if (leasignCombo.getSelectedItem().equals("SN Leasing")) {
                leasingName = "1";
            }
            leasingModel.setLeasingName(leasingName);
            if (leasignCombo.getSelectedItem().equals("SN Leasing")) {
                leasingModel.setDoNo(doNo);
                leasingModel.setGap(gap);
                leasingModel.setMta(mta);
                leasingModel.setMta2(mta2);
                leasingModel.setGs(gs);
                leasingModel.setMta3("0");
                leasingModel.setIdCopy("0");
                leasingModel.setDoNo2("0");
            } else {
                leasingModel.setMta3(mta3);
                leasingModel.setIdCopy(idCopy);
                leasingModel.setDoNo2(doNo2);
                leasingModel.setDoNo("0");
                leasingModel.setGap("0");
                leasingModel.setMta("0");
                leasingModel.setMta2("0");
                leasingModel.setGs("0");
            }
        }

        manageSaleDetail.setPaymentDetail(cashModel, checkModels, leasingModel);
        manageSaleDetail.setPaidHaveSettle(settledPayment.getText(), haveToSettle.getText(), haveToSettlePayment);
        dispose();
    }//GEN-LAST:event_doneButtonActionPerformed

