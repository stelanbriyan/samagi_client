    public void setAmountPaid(double totalAmount, ArrayList<CashModel> cashModels, ArrayList<CheckModel> checkModels, LeasingModel leasingModel) {
        this.totalAmount = totalAmount;
        totalAmountText.setText("Rs " + CurrancyFormat.getCurrancyFormat(totalAmount));
        haveToSettle.setText("Rs " + CurrancyFormat.getCurrancyFormat(totalAmount));

        Requirment r = new Requirment(cashModels, checkModels, leasingModel);
        double paidAmount = r.getTotalAmount();
        this.haveToSettlePayment = totalAmount - paidAmount;
        settledPayment.setText("Rs " + CurrancyFormat.getCurrancyFormat(paidAmount));
        haveToSettle.setText("Rs " + CurrancyFormat.getCurrancyFormat(totalAmount - paidAmount));

        this.checkAmount = r.getChequeAmount();
        this.cashAmount = r.getCashAmount();
        this.leasingAmount = r.getLeasing();

        cashAmountLabel.setText("Rs " + CurrancyFormat.getCurrancyFormat(cashAmount));
        checkAmountLabel.setText("Rs " + CurrancyFormat.getCurrancyFormat(checkAmount));
        leasingAmountLabel.setText("Rs " + CurrancyFormat.getCurrancyFormat(leasingAmount));
    }

    public void setDetail(double totalAmount) {
        this.totalAmount = totalAmount;
        this.haveToSettlePayment = totalAmount;
        totalAmountText.setText("Rs " + CurrancyFormat.getCurrancyFormat(totalAmount));
        haveToSettle.setText("Rs " + CurrancyFormat.getCurrancyFormat(totalAmount));
    }

    public double setCheckAmountLabel() {
        double checkTot = 0;
        for (int i = 0; i < checkTableModel.getRowCount(); i++) {
            checkTot += Double.parseDouble((String) checkTableModel.getValueAt(i, 3));
        }
        checkAmount = checkTot;
        checkAmountLabel.setText("Rs " + CurrancyFormat.getCurrancyFormat(checkTot));
        return checkTot;
    }

    public double getSettledAmount() {
        double settledAmount = checkAmount + cashAmount + leasingAmount;
        settledPayment.setText("Rs " + CurrancyFormat.getCurrancyFormat(settledAmount));
        this.haveToSettlePayment = totalAmount - settledAmount;
        haveToSettle.setText("Rs " + CurrancyFormat.getCurrancyFormat(haveToSettlePayment));
        return settledAmount;
    }

