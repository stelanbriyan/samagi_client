    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        boolean canAdd = true;

        String isIssued = "0";
        if (issued.isSelected()) {
            isIssued = "1";
        }

        if (checkNoText.getText().trim().equals("")) {
            canAdd = false;
        }

        if (amountText.getText().trim().equals("")) {
            canAdd = false;
        }

        if (canAdd) {
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                String generateId = checkModel.getChequeId();

                String type = "";

                if (typeText.getSelectedItem() != null) {
                    type = typeText.getSelectedItem().toString();
                }

                CheckModel checkModel = new CheckModel()
                        .setChequeId(generateId)
                        .setCheckNo(checkNoText.getText())
                        .setIssuedBy(issuedByText.getText())
                        .setAmount(amountText.getText())
                        .setChequeDate(dateFormat.format(dateText.getDate()))
                        .setAccountNo(accountNoText.getText())
                        .setBankName(bankNameText.getSelectedItem().toString())
                        .setStateName(stateText.getText())
                        .setType(type)
                        .setIsIssued(isIssued)
                        .setNote(chequeNote.getText());

                boolean addCheque = ChequeControllerClient.updateCheque(checkModel);
                if (addCheque) {
                    File file = new File(filePath);
                    if (file.isFile()) {
                        byte[] buffer = null;
                        try {
                            InputStream inputStream = new FileInputStream(file);
                            buffer = new byte[inputStream.available()];
                            inputStream.read(buffer);

                            boolean saveFile = FileControllerClient.saveFile("IMG" + generateId + ".cms", buffer);
                        } catch (IOException | NotBoundException ex) {
                            Logger.getLogger(AddNewCheque.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    
                    JOptionPane.showMessageDialog(this, "Cheque detail updated !");
                    manageCheque.getCheque();
                    dispose();
                }
            } catch (NotBoundException | MalformedURLException | ClassNotFoundException | SQLException | RemoteException ex) {
                Logger.getLogger(UpdateChequeDetail.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cheque no & Amount is required. check again !");
        }
    }//GEN-LAST:event_addButtonActionPerformed

