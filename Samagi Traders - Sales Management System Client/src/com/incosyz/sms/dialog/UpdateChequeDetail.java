    public void setChequeModel(CheckModel checkModel) {
        this.checkModel = checkModel;
        checkNoText.setText(checkModel.getCheckNo());
        issuedByText.setText(checkModel.getIssuedBy());
        amountText.setText(checkModel.getAmount());

        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date date = dateFormat.parse(checkModel.getChequeDate());
            dateText.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(UpdateChequeDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
        accountNoText.setText(checkModel.getAccountNo());
        bankNameText.setSelectedItem(checkModel.getBankName());
        stateText.setText(checkModel.getStateName());
        typeText.setSelectedItem(checkModel.getType());
        chequeNote.setText(checkModel.getNote());

        if (checkModel.getIsIssued().equals("1")) {
            issued.setSelected(true);
        }
    }

