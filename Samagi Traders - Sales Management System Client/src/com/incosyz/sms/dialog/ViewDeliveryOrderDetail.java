    private StockModel stockModel;
    private BikeModel bikeModel;

    public void setFramePanel(StockModel model, BikeModel bikeModel) {
        this.stockModel = stockModel;
        this.bikeModel = bikeModel;
        setDetail(model, bikeModel);
    }
    
    public void setDetail(StockModel model, BikeModel bikeModel){
        roundNoLabel.setText(model.getRoundNo());
        modelNameLabel.setText(model.getModelName());
        optionLabel.setText(bikeModel.getBikeOption());
        remarkLabel.setText(model.getRemark());
        
        double commision = Double.parseDouble(bikeModel.getCommision());
        double inclusivePrice = Double.parseDouble(bikeModel.getNetAmount());
        
        commisionLabel.setText(String.valueOf(commision));
        inclusiveReailLabel.setText(String.valueOf(inclusivePrice));
        netRemittanceLabel.setText(String.valueOf(inclusivePrice - commision));
        colourLabel.setText(model.getColour());
        engineLabel.setText(model.getEngineNo());
        chassisLabel.setText(model.getChassisNo());
    }
