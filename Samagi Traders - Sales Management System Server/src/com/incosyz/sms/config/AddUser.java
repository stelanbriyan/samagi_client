    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        String password = String.valueOf(passwordText.getPassword());
        String confirmPassword = String.valueOf(passwordConfirm.getPassword());

        if (password.equals(confirmPassword)) {

            try {
                UserModel userModel = new UserModel();

                String generateId = IDGenerator.getNewId("Users", "userId", "U", 3);
                userModel.setUserId(generateId);
                userModel.setUserName(usernameText.getText());

                userModel.setPassword(password);
                userModel.setUserType((String) typeCombo.getSelectedItem());
                boolean addUser = UserDBController.addUser(userModel);
                if (addUser) {
                    JOptionPane.showMessageDialog(this, "User added successfully !");
                    ServerForm form = new ServerForm();
                    form.setVisible(true);
                    dispose();
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(AddUser.class.getName()).log(Level.SEVERE, null, ex);
            }

        }else{
            JOptionPane.showMessageDialog(this, "Password and confirmpassword is mis matched !");
        }
    }//GEN-LAST:event_doneButtonActionPerformed

